								<div class="image main"><img src="img/pic01.jpg" alt="" /></div>
								<p>Un lugar denominado como "Yacundo" fígura en el mapa Oficial de Bolivia de 1859, en el lugar donde hoy existe la ciudad de Yacuiba, la cual deriva de la lengua guaraní yacú=pavas ibá=laguna o aguada, “aguada de pavas”.</p>

								Yacuiba fue reclamada por la provincia argentina de Salta hasta 1925. Por el Tratado Quirno Costa-Vaca Guzmán del 10 de mayo de 1889, puesto en vigencia el 10 de marzo de 1893, la Argentina y Bolivia fijaron como línea fronteriza el paralelo 22° S. Pero durante la demarcación de la frontera, se descubrió que el pueblo de Yacuiba se hallaba al sur del paralelo 22° S y no al norte, como lo situaban los mapas, por lo que el gobierno de Bolivia realizó una petición a su par argentino para que se rectifique la traza limítrofe en dicho punto, con el objeto de dejar a esa localidad bajo soberanía boliviana, a lo que el gobierno argentino accedió, sin exigir, a cambio de esa cesión soberana, contraprestación territorial alguna.</p>

								Mediante un tratado firmado por representantes de ambos países en el año 1925 se modificó el trazo de la línea de frontera previamente acordada, a fin de que este pueblo y su zona adyacente quedasen dentro de territorio boliviano. Se estableció para ello una suerte de «cuña» de 4,5 km de base (apoyada sobre el paralelo 22° S) con lados que se internan hacia el sur sobre el cauce de dos cursos fluviales, lo que se terminan uniendo a una distancia —en línea recta— de 5990 metros al sur del paralelo 22º S, y que en su interior contiene a la localidad boliviana, y a ambos lados —del otro lado de los arroyos— a la localidad argentina, formada posteriormente, como resultado del comercio de frontera.

								El 23 de marzo de 1899, la ciudad fue sacudida por un fuerte terremoto que causó 3 muertos, varios heridos, y graves repercusiones psicológicas, debido al absoluto desconocimiento de la ocurrencia de este tipo de catástrofe natural.

								Durante la década de 1990 creció demográficamente y económicamente debido al comercio existente en la frontera.

								Hoy es la ciudad más grande del Chaco boliviano, con oficialmente 92.245 habitantes.Yacuiba 92.245[cita requerida] Además, es un importante punto fronterizo entre Bolivia y Argentina. Por último, está conurbada con San José de Pocitos y con Profesor Salvador Mazza (Argentina), separándose de esta última sólo por límites políticos.</p>

