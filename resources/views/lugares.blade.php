										<h2><a>Parroquia<br />
										"San Pedro"</a></h2>
									</header>
									<a href="#" class="image fit"><img src="img/pic03.jpg" alt="" /></a>
									<p>El 23 de marzo de 1898, la pequeña iglesia se derrumbó por un terremoto que sacudió el pueblo de Yacuiba, aprisionado a varios fieles durante el temblor causando la desaparición de la iglesia vieja. Pasado este periodo, se contruyo un golpon a lado de la iglesia destruida para celebrar la misa. Durante la Guerra del Chaco este lugar también sirvió para almacenar los víveres destinados a los combatientes; los fieles escuchaban la misa rodeados de bolsas de harina, arroz, azúcar y otros que se amontonaban en un rincón de tres metros de ancho que daba hasta el altar.
									El padre Félix Brucciaferri, emprendió la construcción del nuevo templo (el que se tiene en la actualidad), en el mismo lugar donde estaba situada la iglesia destruida por el sismo; cuando se procedió a colocar los cimientos del edifico, se descubrieron objetos sepultados durante el desastre ocurrido décadas atrás, como la aparición de monedas, rosarios, imágenes y los más importante (considerado milagroso) fue el hallazgo de la imagen de San Pedro apóstol intacta, por tal razón se nombra a San Pedro como patrono de Yacuiba.</p>
